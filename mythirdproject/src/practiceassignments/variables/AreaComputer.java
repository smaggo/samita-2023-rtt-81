package variables;

public class AreaComputer {
 public static void main(String[] args) {
	 double radius;
	 double area;
	 // Assign a radius
	 radius = 20;
	 // compute area
	 area = radius*radius*3.14159;
	 // Display result
	 System.out.println(" The area of circle of radius "
			 + radius +"is" + area);
	 
 }
}
