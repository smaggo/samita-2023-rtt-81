package variables;

public class Escapecharacters {
 public static void main(String[] args) {
	 //this is example of tab escape character \t
	 System.out.println("This tab escape character ->\t<-");
	 //basic format with tabs
	 System.out.println("1\tone");
	 System.out.print("10\tten\n");
	 System.out.print("100\t\t\tonehundred\n");
	 
	 // this is example to create new line with LINE CHARACTER \n
	 System.out.println("hello everyone");
	 System.out.println("GOOD afternoon to all");
	 
	 // this is example to create just Escape Character called SLASH \
	 System.out.println("to use this character we need 2 of slash \\");
	 
	
 }
}
